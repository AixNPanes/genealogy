/**
 * n DATE <DATE_VALUE>
 */

public class GcDateFamilyEventLine extends Gc_Line
{
	private static AttributeDescriptorMap getAttributeMap()
	{
		return AttributeDescriptorMap.newFromArray(new AttributeDescriptor[]{
				new AttributeDescriptor("DATE_VALUE", 0, 1, GcDateValueAttribute.class),
		});
	}
	
	public GcDateFamilyEventLine(GcBaseElement e)
	{
		super(e, "DATE", getAttributeMap());
	}
};
