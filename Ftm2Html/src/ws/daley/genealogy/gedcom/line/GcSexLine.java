/**
 * used in INDIVIDUAL_RECORD
 * n SEX <SEX_VALUE> 	{0:1}
 */

public class GcSexLine extends Gc_Line
{
	private static AttributeDescriptorMap getAttributeMap()
	{
		return AttributeDescriptorMap.newFromArray(new AttributeDescriptor[]{
				new AttributeDescriptor("SEX_VALUE", 0, 1, GcSexValueAttribute.class)
		});
	}

	public GcSexLine(GcBaseElement e)
	{
		super(e, "SEX", getAttributeMap());
	}
};
